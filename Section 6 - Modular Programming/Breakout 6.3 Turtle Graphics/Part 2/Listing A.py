from turtle import *

# set the appearance of the turtle
hideturtle()
color('red')
pensize(5)

# Draw angle #1
backward(100)
left(30)
forward(100)

# Position the pen
penup()
setpos(200, 0)
setheading(0)
pendown()

# Draw angle #2
backward(100)
left(60)
forward(150)





